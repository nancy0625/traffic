public class Car {
    public int CarId;
    public String NowDate;
    public int Money;

    public int getCarId() {
        return CarId;
    }

    public void setCarId(int carId) {
        CarId = carId;
    }

    public String getNowDate() {
        return NowDate;
    }

    public void setNowDate(String nowDate) {
        NowDate = nowDate;
    }

    public int getMoney() {
        return Money;
    }

    public void setMoney(int money) {
        Money = money;
    }
}
